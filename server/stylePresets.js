/**
 * 次元に基づいてスタイル選択肢を取得
 */
function getStyleOptions(dimension) {
  const dim = dimension === '3d' ? '3d' : '2d';
  return visualStyles[dim];
}

/**
 * スタイルIDからスタイル情報を取得
 */
function getStyleById(dimension, styleId) {
  const dim = dimension === '3d' ? '3d' : '2d';
  return visualStyles[dim].find(s => s.id === styleId);
}

module.exports = {
  visualStyles,
  getStyleOptions,
  getStyleById
};
